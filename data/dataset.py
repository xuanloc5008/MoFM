"""
ACDC Dataset — loads from preprocessed .npz cache.
Run preprocess.py first to generate the cache.
"""
import numpy as np
import torch
import json
import pickle
from torch.utils.data import Dataset
from typing import Dict, Tuple, Optional
from pathlib import Path


class ACDCDataset(Dataset):
    """ACDC dataset loading from preprocessed .npz cache.

    Expected cache structure (generated by preprocess.py):
        cache_dir/
        ├── train/
        │   ├── patient001_ED_slice00.npz  (image + mask)
        │   ├── metadata.json
        │   ├── persistence_diagrams/      (optional)
        │   └── encoder_features/          (optional)
        ├── val/
        └── test/
    """

    STRUCTURE_MAP = {"LV": 1, "Myo": 2, "RV": 3}

    def __init__(self, cache_dir: str, split: str = "train",
                 augment: bool = True, tpa_enabled: bool = True,
                 max_tpa_retries: int = 10,
                 use_cached_features: bool = False,
                 use_cached_pds: bool = True):
        self.cache_dir = Path(cache_dir)
        self.split_dir = self.cache_dir / split
        self.split = split
        self.augment = augment and split == "train"
        self.tpa_enabled = tpa_enabled
        self.max_tpa_retries = max_tpa_retries

        # Load metadata
        meta_file = self.split_dir / "metadata.json"
        if not meta_file.exists():
            raise FileNotFoundError(
                f"Metadata not found: {meta_file}\n"
                f"Run: python preprocess.py --data_root <ACDC_TRAINING_PATH>"
            )
        with open(meta_file) as f:
            self.metadata = json.load(f)

        # Index npz files
        self.npz_files = []
        for item in self.metadata:
            p = self.split_dir / f"{item['slice_id']}.npz"
            if p.exists():
                self.npz_files.append(p)
        assert len(self.npz_files) > 0, f"No .npz in {self.split_dir}. Run preprocess.py."

        # Optional caches
        self.feat_dir = self.split_dir / "encoder_features"
        self.pd_dir = self.split_dir / "persistence_diagrams"
        self.has_features = self.feat_dir.exists() and use_cached_features
        self.has_pds = self.pd_dir.exists() and use_cached_pds

        print(f"ACDCDataset [{split}]: {len(self)} slices | "
              f"features={'Y' if self.has_features else 'N'} | "
              f"PDs={'Y' if self.has_pds else 'N'}")

    def __len__(self):
        return len(self.npz_files)

    def __getitem__(self, idx):
        npz_path = self.npz_files[idx]
        meta = self.metadata[idx]
        data = np.load(npz_path)
        image = data["image"].astype(np.float32)
        mask = data["mask"].astype(np.int32)

        if self.augment:
            image, mask = self._augment(image, mask)

        output = {
            "image": torch.from_numpy(image).float().unsqueeze(0),
            "mask": torch.from_numpy(mask).long(),
            "patient_id": meta["patient_id"],
            "slice_idx": meta["slice_idx"],
            "z_norm": meta["z_norm"],
            "phase": meta["phase"],
            "group": meta.get("group", "unknown"),
            "pixel_spacing": meta.get("pixel_spacing", [1.0, 1.0]),
            "slice_thickness": meta.get("slice_thickness", 10.0),
            "idx": idx,
        }

        if self.has_features:
            feat_path = self.feat_dir / f"{npz_path.stem}_feat.npy"
            if feat_path.exists():
                output["encoder_features"] = torch.from_numpy(np.load(feat_path)).float()

        return output

    # ---- Augmentation ----

    def _augment(self, image, mask):
        for _ in range(self.max_tpa_retries):
            img_a, mask_a = self._apply_random_aug(image.copy(), mask.copy())
            if not self.tpa_enabled or self._topo_ok(mask_a):
                return img_a, mask_a
        return image, mask

    def _apply_random_aug(self, image, mask):
        aug = np.random.choice(
            ["elastic", "rotation", "intensity", "noise", "blur", "none"],
            p=[0.25, 0.2, 0.15, 0.15, 0.1, 0.15])

        if aug == "elastic":
            return self._elastic(image, mask)
        elif aug == "rotation":
            return self._rotate(image, mask)
        elif aug == "intensity":
            return np.clip(image * np.random.uniform(0.8, 1.2), 0, 1), mask
        elif aug == "noise":
            return np.clip(image + np.random.normal(0, 0.03, image.shape), 0, 1), mask
        elif aug == "blur":
            from scipy.ndimage import gaussian_filter
            return gaussian_filter(image, np.random.uniform(0.5, 1.5)), mask
        return image, mask

    def _elastic(self, image, mask):
        from scipy.ndimage import gaussian_filter, map_coordinates
        h, w = image.shape
        s, a = np.random.uniform(10, 30), np.random.uniform(50, 200)
        dx = gaussian_filter(np.random.randn(h, w), s) * a
        dy = gaussian_filter(np.random.randn(h, w), s) * a
        y, x = np.meshgrid(np.arange(h), np.arange(w), indexing='ij')
        idx = [np.clip(y + dy, 0, h - 1), np.clip(x + dx, 0, w - 1)]
        return map_coordinates(image, idx, order=3, mode='reflect'), \
               map_coordinates(mask, idx, order=0, mode='reflect').astype(np.int32)

    def _rotate(self, image, mask):
        from scipy.ndimage import rotate
        a = np.random.uniform(-20, 20)
        return np.clip(rotate(image, a, reshape=False, order=3), 0, 1), \
               rotate(mask, a, reshape=False, order=0).astype(np.int32)

    def _topo_ok(self, mask):
        from scipy.ndimage import label as scipy_label
        for idx in [1, 2, 3]:
            b = (mask == idx).astype(np.uint8)
            if b.sum() == 0:
                continue
            _, n = scipy_label(b)
            if n != 1:
                return False
        return True

    # ---- PD helpers ----

    def get_pd(self, idx):
        if not self.has_pds:
            return None
        p = self.pd_dir / f"{self.npz_files[idx].stem}_pd.pkl"
        if p.exists():
            with open(p, "rb") as f:
                return pickle.load(f)
        return None

    def get_pd_similarity(self, i, j, structure="Myo"):
        from data.topology import pd_similarity
        pd_i, pd_j = self.get_pd(i), self.get_pd(j)
        if pd_i is None or pd_j is None:
            return 0.0
        return pd_similarity(pd_i.get(structure, []), pd_j.get(structure, []))


def collate_fn(batch):
    result = {
        "image": torch.stack([b["image"] for b in batch]),
        "mask": torch.stack([b["mask"] for b in batch]),
        "patient_id": [b["patient_id"] for b in batch],
        "slice_idx": [b["slice_idx"] for b in batch],
        "z_norm": torch.tensor([b["z_norm"] for b in batch]),
        "phase": [b["phase"] for b in batch],
        "idx": [b["idx"] for b in batch],
    }
    if "encoder_features" in batch[0]:
        result["encoder_features"] = torch.stack([b["encoder_features"] for b in batch])
    return result
