"""
ACDC Dataset — loads from preprocessed .npz cache.
Run preprocess.py first to generate the cache.
"""
import numpy as np
import torch
import json
import pickle
from torch.utils.data import Dataset
from typing import Dict, Tuple, Optional
from pathlib import Path


class ACDCDataset(Dataset):
    """ACDC dataset loading from preprocessed .npz cache.

    Expected cache structure (generated by preprocess.py):
        cache_dir/
        ├── train/
        │   ├── patient001_ED_slice00.npz  (image + mask)
        │   ├── metadata.json
        │   ├── persistence_diagrams/      (optional)
        │   └── encoder_features/          (optional)
        ├── val/
        └── test/
    """

    STRUCTURE_MAP = {"LV": 1, "Myo": 2, "RV": 3}

    def __init__(self, cache_dir: str, split: str = "train",
                 augment: bool = True, tpa_enabled: bool = True,
                 max_tpa_retries: int = 10,
                 use_cached_features: bool = False,
                 use_cached_pds: bool = True):
        self.cache_dir = Path(cache_dir)
        self.split_dir = self.cache_dir / split
        self.split = split
        self.augment = augment and split == "train"
        self.tpa_enabled = tpa_enabled
        self.max_tpa_retries = max_tpa_retries

        # Load metadata
        meta_file = self.split_dir / "metadata.json"
        if not meta_file.exists():
            raise FileNotFoundError(
                f"Metadata not found: {meta_file}\n"
                f"Run: python preprocess.py --data_root <ACDC_TRAINING_PATH>"
            )
        with open(meta_file) as f:
            self.metadata = json.load(f)

        # Index npz files
        self.npz_files = []
        for item in self.metadata:
            p = self.split_dir / f"{item['slice_id']}.npz"
            if p.exists():
                self.npz_files.append(p)
        assert len(self.npz_files) > 0, f"No .npz in {self.split_dir}. Run preprocess.py."

        # Optional caches
        self.feat_dir = self.split_dir / "encoder_features"
        self.pd_dir = self.split_dir / "persistence_diagrams"
        self.has_features = self.feat_dir.exists() and use_cached_features
        self.has_pds = self.pd_dir.exists() and use_cached_pds

        print(f"ACDCDataset [{split}]: {len(self)} slices | "
              f"features={'Y' if self.has_features else 'N'} | "
              f"PDs={'Y' if self.has_pds else 'N'}")

    def __len__(self):
        return len(self.npz_files)

    def __getitem__(self, idx):
        npz_path = self.npz_files[idx]
        meta = self.metadata[idx]
        data = np.load(npz_path)
        image = data["image"].astype(np.float32)
        mask = data["mask"].astype(np.int32)

        if self.augment:
            image, mask = self._augment(image, mask)

        output = {
            "image": torch.from_numpy(image).float().unsqueeze(0),
            "mask": torch.from_numpy(mask).long(),
            "patient_id": meta["patient_id"],
            "slice_idx": meta["slice_idx"],
            "z_norm": meta["z_norm"],
            "phase": meta["phase"],
            "group": meta.get("group", "unknown"),
            "pixel_spacing": meta.get("pixel_spacing", [1.0, 1.0]),
            "slice_thickness": meta.get("slice_thickness", 10.0),
            "idx": idx,
        }

        if self.has_features:
            feat_path = self.feat_dir / f"{npz_path.stem}_feat.npy"
            if feat_path.exists():
                output["encoder_features"] = torch.from_numpy(np.load(feat_path)).float()

        return output

    # ---- Augmentation (nnU-Net-style pipeline) ----
    # Key difference from before: augmentations are applied as a CHAIN with
    # independent probabilities (not mutually exclusive). This follows nnU-Net
    # which is proven across 50+ medical segmentation benchmarks including ACDC.

    def _augment(self, image, mask):
        """Apply augmentation chain. If TPA enabled, retry until topology preserved."""
        for _ in range(self.max_tpa_retries):
            img_a, mask_a = self._augment_chain(image.copy(), mask.copy())
            if not self.tpa_enabled or self._topo_ok(mask_a):
                return img_a, mask_a
        return image, mask

    def _augment_chain(self, image, mask):
        """nnU-Net-style augmentation chain. Each augmentation applied independently."""

        # 1. Spatial: rotation + scaling (combined, p=0.3)
        if np.random.rand() < 0.3:
            image, mask = self._rotate_scale(image, mask)

        # 2. Spatial: elastic deformation (p=0.2)
        if np.random.rand() < 0.2:
            image, mask = self._elastic(image, mask)

        # 3. Spatial: mirroring/flip (p=0.5 each axis)
        if np.random.rand() < 0.5:
            image, mask = image[:, ::-1].copy(), mask[:, ::-1].copy()  # horizontal
        if np.random.rand() < 0.5:
            image, mask = image[::-1, :].copy(), mask[::-1, :].copy()  # vertical

        # 4. Intensity: gamma correction (p=0.3)
        #    Simulates MRI intensity inhomogeneity. Proven critical for MRI.
        if np.random.rand() < 0.3:
            image = self._gamma_correction(image)

        # 5. Intensity: brightness multiplicative (p=0.2)
        if np.random.rand() < 0.2:
            image = np.clip(image * np.random.uniform(0.75, 1.25), 0, 1)

        # 6. Intensity: contrast adjustment (p=0.2)
        if np.random.rand() < 0.2:
            image = self._adjust_contrast(image)

        # 7. Intensity: Gaussian noise (p=0.15)
        if np.random.rand() < 0.15:
            image = np.clip(image + np.random.normal(0, 0.03, image.shape), 0, 1)

        # 8. Intensity: Gaussian blur (p=0.15)
        if np.random.rand() < 0.15:
            from scipy.ndimage import gaussian_filter
            image = gaussian_filter(image, np.random.uniform(0.5, 1.5))

        return image.astype(np.float32), mask.astype(np.int32)

    def _rotate_scale(self, image, mask):
        """Combined rotation + scaling (nnU-Net style)."""
        from scipy.ndimage import affine_transform
        h, w = image.shape
        angle = np.random.uniform(-15, 15)  # degrees
        scale = np.random.uniform(0.85, 1.15)

        # Build affine matrix
        rad = np.deg2rad(angle)
        cos_a, sin_a = np.cos(rad) / scale, np.sin(rad) / scale
        center = np.array([h / 2, w / 2])
        matrix = np.array([[cos_a, -sin_a], [sin_a, cos_a]])
        offset = center - matrix @ center

        img_t = affine_transform(image, matrix, offset, order=3, mode='nearest')
        mask_t = affine_transform(mask, matrix, offset, order=0, mode='nearest')
        return np.clip(img_t, 0, 1), mask_t.astype(np.int32)

    def _elastic(self, image, mask):
        """Elastic deformation with random smooth displacement field."""
        from scipy.ndimage import gaussian_filter, map_coordinates
        h, w = image.shape
        sigma = np.random.uniform(12, 25)
        alpha = np.random.uniform(60, 180)
        dx = gaussian_filter(np.random.randn(h, w), sigma) * alpha
        dy = gaussian_filter(np.random.randn(h, w), sigma) * alpha
        y, x = np.meshgrid(np.arange(h), np.arange(w), indexing='ij')
        indices = [np.clip(y + dy, 0, h - 1), np.clip(x + dx, 0, w - 1)]
        return (map_coordinates(image, indices, order=3, mode='reflect'),
                map_coordinates(mask, indices, order=0, mode='reflect').astype(np.int32))

    def _gamma_correction(self, image):
        """Gamma correction — simulates MRI intensity inhomogeneity.
        gamma < 1: brighter (enhances low-intensity regions)
        gamma > 1: darker (enhances high-intensity regions)
        nnU-Net applies this with 30% probability."""
        gamma = np.random.uniform(0.7, 1.5)
        # Ensure image is in [0, 1] range (already should be after preprocessing)
        return np.clip(np.power(image + 1e-8, gamma), 0, 1)

    def _adjust_contrast(self, image):
        """Contrast adjustment: blend with mean intensity.
        factor > 1: increase contrast, factor < 1: decrease contrast."""
        factor = np.random.uniform(0.75, 1.25)
        mean = image.mean()
        return np.clip(mean + factor * (image - mean), 0, 1)

    def _topo_ok(self, mask):
        from scipy.ndimage import label as scipy_label
        for idx in [1, 2, 3]:
            b = (mask == idx).astype(np.uint8)
            if b.sum() == 0:
                continue
            _, n = scipy_label(b)
            if n != 1:
                return False
        return True

    # ---- PD helpers ----

    def get_pd(self, idx):
        if not self.has_pds:
            return None
        p = self.pd_dir / f"{self.npz_files[idx].stem}_pd.pkl"
        if p.exists():
            with open(p, "rb") as f:
                return pickle.load(f)
        return None

    def get_pd_similarity(self, i, j, structure="Myo"):
        from data.topology import pd_similarity
        pd_i, pd_j = self.get_pd(i), self.get_pd(j)
        if pd_i is None or pd_j is None:
            return 0.0
        return pd_similarity(pd_i.get(structure, []), pd_j.get(structure, []))


def collate_fn(batch):
    result = {
        "image": torch.stack([b["image"] for b in batch]),
        "mask": torch.stack([b["mask"] for b in batch]),
        "patient_id": [b["patient_id"] for b in batch],
        "slice_idx": [b["slice_idx"] for b in batch],
        "z_norm": torch.tensor([b["z_norm"] for b in batch]),
        "phase": [b["phase"] for b in batch],
        "idx": [b["idx"] for b in batch],
    }
    if "encoder_features" in batch[0]:
        result["encoder_features"] = torch.stack([b["encoder_features"] for b in batch])
    return result
